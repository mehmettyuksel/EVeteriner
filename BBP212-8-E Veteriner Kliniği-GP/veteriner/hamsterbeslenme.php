	   Hamsterlarda beslenme çok dikkat edilmesi gereken bir husustur. Çok obur olmalarına karşın verilen gıdalara da dikkat edilmesi gerekmektedir. Hamsterler omnivor hayvanlardır. Golden hamsterlerin tabii ortamda ne ile beslendikleri hakkında fazla bilgi olmamakla beraber; bunların bitki tohumları, böğürtlen ve çilek gibi sulu meyveler yedikleri kabul edilmektedir.
Laboratuar hayvanları için hazırlanmış ticari pelet yemlerden hamsterlere günlük 10 gr. vermek yeterlidir. Su ihtiyaçları 8-10 ml kadardır (veya 100 gr canlı ağırlık için 10 ml) su sürekli, temiz ve taze olarak şişeler veya otomatik suluklarda verilmelidir.
