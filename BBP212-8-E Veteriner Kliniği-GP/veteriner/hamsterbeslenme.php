	   <br>
	   <p>
	   Hamsterlar kemirgen hayvanlardır. Yiyeceklerini kemirerek ve yavaş yavaş yerler. Fındık fıstık tarzı sert gıdalardan hoşlanırlar. Çene yapılarından dolayı gıda depolama keseleri ve sindirim sistemleri yapılarından dolayı yumuşak gıdalar hamsterlar için uygun değildir.
	  
	  Hamsterlar vereceğiniz yiyecekleri ağızlarının içinde bulunan keselerde depolarlar. Daha sonrasında depoladıkları yiyecekleri evlerine taşır ve saklarlar. Yiyecekleri zamana kendileri karar verirler. Yumuşak gıdalar onlara göre olmasa bile, yumuşak gıdalara hayır demezler ancak bu durumun birkaç olumsuz etkisi görülebilir;
<br><br>
<ul >
<li> Yumuşak gıdalar tüketen hamsterlar diş uzamasına maruz kalırlar, bu durum onların bir süre sonra yem yiyemez hale gelmesine sebebiyet verir. Sert gıdalar kemirgen hamsterların dişlerinde törpü etkisi yarattığı için keskin ve kolay öğütülebilir bir hal alır.</li>

<li> Yumuşak gıdaların depolama kesesinden çıkarılması çok zordur.</li>

<li> Hamsterlar yumuşak gıdaları yuvalarına sakladıklarında , yumuşak gıdalar hızlıca çürür ve kokuya sebep olur.</li>

<li> Yumuşak gıdalarda su oranı fazla olduğundan hamsterların sindirim sistemine fazlasıyla zarar verir.</li>
	   </ul><br>

	   </p>
	   <p >
	   Su her canlıda olduğu gibi hamsterlar için de su oldukça önemlidir. Özellikle fındık fıstık gibi gıdalarla beslenen hamsterlar için su oldukça önemlidir. Yine de hamsterlar fazla su içmezler. Satın alabileceğiniz hamsterlar için özel üretilmiş yemler gıda için uygun olacaktır. Ay çekirdeği hamsterların favorileri arasındadır. Fındık, fıstık, ceviz içi, kuşyemi, darı, mısır, kuru kedi ve köpek mamaları, bisküvi, muhabbet kuşu ve papağan krakerleri, bayat ekmek, buğday hamsterlerın severek tükettiği sert gıdalardandır. Hamsterlarınıza haftada iki veya üç defa mavi lahana, ıspanak, havuç, elma, patates gibi meyve ve sebzeler, haşlanmış yumurta gibi vitamin takviyesinde bulunabilirsiniz. 

Yeni doğan hamster yavruları gözleri açılana kadar anne sütü ile beslenirler. Yeni yeni yem tüketmeye başlayan yavru hasmterlarınıza haşlanmış yumurta ve süt tozu ile hazırladığınız karışımları vermeniz oldukça faydalı olacaktır.

Hamsterlarınıza kesinlikle vermemeniz gereken gıdalar vardır. Bunların en başında çiğ et yer alır. Portakal, mandalina, limon, kavun ve karpuz gibi çok sulu meyveler de hamster için iyi değildir. Ayrıca soğan ve sarımsak türleri de hamsterlara kesinlikle verilmemesi gereken gıdalardandır.
	   </p>
	   <br><br>
	   <img  src="blogresim/hamsterbeslenme1.jpg" class="rsm">
	   <br><br>
	   <p >
	   Hamsterlar, hem etle hem de sütle beslenen omnivor canlılardır. Doğada yaşayan hamsterler bitki ve tohumlarını yedikleri gibi diğer yandan da böcekleri yerler. Hamsterlar yemek yemekten çok büyük zevk alırlar. Her ne kadar evcilleştirilmiş de olsalar tıpkı vahşi ataları gibi içgüdü ve metabolizmaya sahiptirler. Hamsterınızın yeteri kadar mutlu ve sağlıklı olmasını istiyorsanız, ona çok geniş bir yiyecek yelpazesi sunmanız gereklidir.

Öncelikle hamsterlar için özel olarak bulunan su kaplarından almalısınız. Su kabının kafese mutlaka sabitlenmiş olması gereklidir. Düzenli olarak suyunu değişmelisiniz ve suyunun temizliğini korumaya büyük bir özen göstermelisiniz. İçecek olarak suyun haricinde hafif sulandırılmış süt verebilirsiniz. Hamsterlar neyi ne zaman yiyeceklerine kendileri karar verir. Bu yüzden sizin onlara verdiğiniz yiyecekleri yanaklarındaki keseciklere koyarak güvenilir buldukları köşelerine yığıp üstünü de talaşla örterler. Bu yüzden çabuk bozulan, sararan yiyeceklerden çok az vermelisiniz -peynir elma vb.-  Çünkü eğer çürümüş besinleri yerlerse zehirlenebilme riskleri vardır. Hamsterlar için özel üretilen gıdalar, yemler vardır. Bu yemleri hamsterlar büyük bir iştahla yerler. Ancak bu yemin haricinde sizin de hamsterınıza ek olarak verebileceğiniz gıdalar mevcuttur. Elma, avokado, muz, çilek, böğürtlen, brokoli, mısır, üzüm, havuç, mango, kavun, karpuz gibi bütün taze meyve ve sebzeleri hamsterınıza gönül rahatlığı ile verebilirsiniz. Ancak her şeyin bir ölçüsü olduğu gibi bunun da bir ölçüsü vardır. Fazla meyve, sebze verirseniz hayvanınızı ishal yapıp, hastalandırabilirsiniz. Hamsterlar için zararlı olan yiyecekler de vardır. Çikolata, şekerleme, çiğ bezelye, çiğ patates, tavşan yemleri, domates yaprakları, soğan ve sarımsak gibi besin maddeleri hamsterlara verilmemesi gereken yiyeceklerin başında gelmektedir.

Hamsterların beslenme saatleri ile ilgili 2 teori vardır. Bunlardan ilki sabahları yemek verilmesi ikincisi ise akşamları verilmesi yönündedir. Hamsterlar gündüzleri uyuyan, geceleri aktif olan hayvanlardır. Ancak gün içinde yaklaşık 1,5-2 saatte bir uyanarak yemek yedikleri bilinir. Bu neden gün içinde de besine ihtiyaç duymaktadırlar. Zaten doğal hayatlarında geceleri aktif olsalar bile geceleri toplayıp yuvalarında biriktirdikleri besin maddelerini gündüzleri tüketmektedirler.
	   </p>
