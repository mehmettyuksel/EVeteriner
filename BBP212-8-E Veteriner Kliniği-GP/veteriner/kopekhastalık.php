	   <h2  >7. Kuduz</h2>
	   <br>
	   <p >
	   Herhangi bir memeli hayvan kuduza neden olan virüsle enfekte olabilmektedir. Köpeklerde kuduz virüsünün aşılanma ile önlenebileceği gibi tedavi edilmemesi durumunda ölümle sonuçlanabilmektedir. Virüs genellikle enfekte bir hayvandan gelen ısırık ya da tükürük yoluyla kolaylıkla bulaşabilmektedir.
	   </p>
	   <br>
	   <h2  >8. Saçkıran</h2>
	   <br>
	   <p >
	   Köpeklerde saçkıran hastalığı ilk bakıştan bir solucan olduğu düşünülse de aksine ciltte meydana gelen mantar enfeksiyonlarından kaynaklanmaktadır. Enfekte bir köpek temasıyla bulaşabilen saçkıran hastalığı, genellikle dairesel tüy dökülmelerine sebep olmaktadır. Zamanla kendiliğinden iyileşebilen saçkıran hastalığının diğer köpeklere ve insanlara bulaşmasını önlemek için tedavi edilmesi gerekmektedir.
	   </p>
	   <br>
	   <h2  >9. Kanser</h2>
	   <br>
	   <p >
	   Köpeklerde kanser hastalığı, hücrelerin kontrolsüz bir şekilde büyüdüğü, çevresindeki dokuyu istila ettiği ve vücudun diğer bölgelerine yayılabilen ölümcül bir hastalık türüdür. İnsanlarda olduğu gibi, köpeklerde de çeşitli kanser türleri görülebilmektedir. Hastalık bir tümör gibi tek bir bölgeyle sınırlı kalabileceği gibi tüm vücuda yayılabilir.
	   </p>
	   <br><br>
	   <img src="blogresim/kopekhastalık4.jpg" class="rsm">
	   <br><br>
	   <h2  >10. Kalp Kurdu</h2>
	   <p >
	   Heartworm olarak da bilinen kalp kurdu, enfekte sivrisinekler tarafından yayılan ve köpeklere kolaylıkla bulaşabilen bir hastalık türüdür. Kalp kurdu birçok köpekte meydana gelen hastalıkların temel sebebi olarak bilinmesiyle beraberinde bu hastalık köpekler için antipik (gelişimsel bozukluk) bir konaktır. Kalp kurdu, köpeklerde ilk olarak akciğer hastalığına sebep olmaktadır. Özellikle sivrisineklerin yoğun olduğu bölgelerde yaşayan köpek sahiplerinin endişe verici bu hastalık için veteriner hekime başvurması gerekmektedir.
	   </p>
	   <br>
	   <h2  >11. Köpeklerde Katarakt Hastalığı</h2>
	   <br>
	   <p >
	    Daha çok ileri yaşlarda görülmesine rağmen, her yaşta ve ırkta karşımıza çıkabilmektedir. Ancak, bazı ırklar katarakta daha hassastır. Sıkça görülmesine karşın nasıl oluştuğuna ait mekanizma tam olarak bulunamamıştır. Bozulma lensin saydam yapısının matlaşmasıyla başlar şeffaf olan yapı her geçen gün bozulur ve bunun sonucunda görme bozukluğu ortaya çıkar. Birkaç değişik formda ortaya çıkabilen katarakt, çoğunlukla 6-7'li yaşlarda başlar; daha önce görülmesi ırksal hassasiyet nedeniyledir. Hassas olduğu bahsedilen bu ırklara; Chow chow, Yorshire terrier, Doberman, Pekingese örnek verilebilir. Bunun dışında katarakt'ın ortaya çıkış nedeni olarak şeker hastalığı ve travmalar sayılabilir. Gözde belirgin bir matlaşma ilk fark edilen belirtidir, hasta geceleri gündüze oranla çok daha iyi görür. Gözde bulanıklık ve matlaşma fark edildiğinde, erken teşhisle ilerleme durdurulabilir ya da operasyon ile tamamen görme kaybı engellenebilir.
		</p>
	   
	   
	   
	   
