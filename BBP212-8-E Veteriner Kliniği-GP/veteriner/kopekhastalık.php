	   <h2 >1. Şeker Hastalığı</h2>
	   <br>
	   <p >
	   Köpeklerde diyabet, insülin hormonu eksikliği veya insüline yetersiz yanıtın neden olduğu karmaşık bir hastalık çeşitidir. Köpeğin sindirim sistemi, besinleri glikoz dahil olmak üzere çeşitli bileşenlere böler ve hücrelerine insülin taşınır. Bir köpek insülin üretmediğinde veya insülini normal şekilde kullanamadığında, kan şekeri seviyesi yükselmektedir. Sonuç olarak tedavi edilmediği taktirde birçok hastalığa sebep olabileceği gibi köpeklerde hiperglisemi görülebilmektedir.
	   </p>
	   <br>
	   <h2  >2. Bağırsak Parazitleri</h2>
	   <br>
	   <p >
	   Bağırsak parazitleri, köpeklerde en sık görülen hastalık çeşitleri arasında yer almaktadır. Yuvarlak kurtlar, kancalı kurtlar, kırbaç kurtlar ve tenya kurtları gibi farklı oluşumlarda görülebilen bağırsak parazitleri köpeklerin dışkısından, kirlenmiş kürk ve pençelerin yalanmasıyla bulaşmaktadır. Bağırsaklarda meydana gelen kurt oluşumları, köpeğinizin beslenmesini etkileyeceği gibi kusma ve ishal gibi sorunlarıda beraberinde getirmektedir. Köpeğinizin bağırsak parazitlerine karşı dirençli olması için aşılarının yapılması gerekmektedir.
	  </p>
	  <br>
	  <h2  >3. Kennel Öksürük</h2>
	  <br>
	  <p >
	   Kennel öksürük, köpeklerde ses ve soluk borusunun iltihaplanmasına neden olan bir çeşit solunum yolu enfeksiyonudur. Son derece bulaşıcı bir köpek hastalığı olan kennel öksürük, virisün köpeklerle temas etmesi durumunda canlılık göstermektedir. Hem viral hem bakteriyel bir enfeksiyon çeşiti olan bu hastalık, tedavi edilmemesi durumunda ciğerleri büyük ölçüde tahribata uğratmaktadır. Köpeklerde kennel öksürük, insanlarda soğuk algınlığa benzemektedir.
	   </p>
	   <br><br>
	   <img src="blogresim/kopekhastalık2.jpg" class="rsm">
	   <br><br>
	   <h2 >4. Dış Parazit</h2>
	   <br>
	   <p >
	   Köpeklerde sıklıkla rastlanılan dış parazit hastalıkları, genellikle kene ve pire gibi parazitlerden meydana gelmektedir. kene ve pire gibi parazitlerin köpek tüylerine nüfus etmesiyle yayılım gösteren bu hastalık, ilerleyen zamanda ciddi köpek problemlerine sebep olabilmektedir. Köpeğinizin dış parazitlerden korunması için sayısız tedavi yöntemi ve ürün bulunurken veteriner tarafından dış parazit aşılarının yapılması son derece önemlidir.
	   </p>
	   <br>
	   <h2  >5. Parvovirüs</h2>
	   <br>
	   <p >
	   Köpeklerde parvovirüs, hayatı ciddi ölçüde tehdit eden hastalıkların oluşumuna sebep olabilen bulaşıcı bir virüs hastalığıdır. Parvo, köpeklerde gastrointestinal sistemine saldırarak ateşe, kusmaya, ağrıya ve genellikle kanlı ishale neden olmaktadır. Hijyen olmayan pis ortamlarda kolaylıkla yayılım gösteren parvovirüs, uzunca yıllar toprakda yaşayabilmesinden dolayı yok edilmesi son derece zordur.
	   </p>
	   <br>
	   <h2 >6. Leptospira</h2>
	   <br>
	   <p >
	   Leptospira, köpeklerde ciddi hastalık sorunlarına yol açan az sayıda patojenik ve saprofitik türe sahip bir çeşit bakteri çeşitidir. Bu bakteriler köpeklere genellike kirli su tüketimi, kirli toprak veya yiyeceklerle temas edilmesi sonucunda enfekte olmaktadır. Leptospira ile enfekte olan köpeklerde; ateş, kas ağrısı, kusma, uyuşukluk, karın ağrısı, böbrek ve karaciğer yetmezliği gelişebilmektedir. Köpeğinizin Leptospira baktarisine karşı savunma alması için veteriner tarafından aşılanması gerekmektedir.
 
	   </p>
	   <br><br>
	   <img  src="blogresim/kopekhastalık3.jpg" class="rsm">
