	   <br>
	  <p >
	   İdrar yolu enfeksiyonları daha çok erkek kedilerde görülmektedir. Kedilerde görülen yaygın rahatsızlıklardan birisidir. Hayvanlar idrar yaparken zorlanır ve idrarları kanlı olarak gelir. Veterinere gösterilmesi gerekmektedir.
	   </p>
