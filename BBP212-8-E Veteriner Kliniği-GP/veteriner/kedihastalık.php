	   <h2 > 9. Diyabet</h2>
	    <br>
	   <p >
	   Son yıllarda kedilerde görülmeye başlayan hastalık türlerinden birisidir. Kediler çok su içmek istiyorsa ve çok su içiyorsa bu diyabet hastalığı belirtisi olabilir. Kedilerin kan değerleri ile ortaya çıkmaktadır. Bağışıklık sisteminin zayıflamasıyla birlikte görülebilir.Sadece insanlar değil kediler de diyabet hastası olabilir. Fakat bu genellikle yaşlı aşırı kilolu kedilerde görülür. Belirtileri ise artan susuzluk ve işeme, kum kabının dışına işeme, uyuşukluk ve depresyon içerir.
  Kedilerde diyabetin sebebi kesin olarak bilinmemekle birlikte diyabet aşırı kiloya bağlantısı vardır. Bu nedenle diyabet tedavisi mama değişikliği ve kedilerin ihtiyaçları bağlı olarak ağız yoluyla ilaç tedavisi veya iğneleri de içerir.
	   </p>
	    <br>
	   <h2 > 10. Kulak Yolu İltihabı</h2>
	    <br>
	   <p >
	   Kedilerde en sık görülen rahatsızlıklar arasında kulak yolu iltihabı hastalığıdır. Kulak yolu iltihabi çeşitli virüsler ile bulaşmakta ve mikroorganizmalara bağlı olarak gelişen bir hastalık türüdür. Kedilerde kulak enfeksiyonu hastalığının birkaç nedeni vardır. Akıntı, bakteri, diyabet, alerjiler ve ilaca karşı reaksiyonlar içerebilir. Bazı kedi ırkları kulak enfeksiyonlarına daha duyarlıdır. Evcil hayvanınızda kulak akıntısı, kafa sallama, kulak kapaklarında şişme ve kokma varsa, kulağına dokunulduğunda aşırı tepki veriyorsa kedinizde kulak enfeksiyonu vardır. Tedavisi için kulak tıkaçları, kulak temizleyici, kulak ve ağız yoluyla ilaç alımı ve ciddi vakalarda ameliyat gerekecektir. Doğru bir tedavi programı için veteriner hekiminize danışınız.
	   
	   </p>
