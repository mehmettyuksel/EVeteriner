	  <h2 >  5.Bağırsak İltihabı ve Kabızlık</h2>
	   <br>
	   <p >
	     Kediler dışkısını yaparken zorlanıyor, iştahsızlık çekiyor, su kaybetme ve kabızlık belirtisi yaşıyorsa bu belirtiler kabızlık belirtisidir. Kediniz kabız olduğunda veteriner hekiminiz, bu hastalığın altında yatan nedeni test eder ve ona göre bir tedavi planı size sunar. Bu tedavi genelde lif açısından zengin bir mama, kurtları yok etmek, antibiyotik kullanmak, bağırsak yumuşatıcı ve bol sıvı kullanımı içerir. 
	   </p>
	    <br>
	  <h2 > 6. Mide Enfeksiyonu</h2>
	   <br>
	   <p >
	   En yaygın kedi hastalıklarından birisidir. Kedilere bu hastalık genellikle mamalardan geçmektedir. Mamalarda bulunan çeşitli virüsler neden olmaktadır. mama alırken bilinen kaliteli ürünler seçilmelidir. Kedinin mide zarının iltihaplanması gastrit olarak adlandırılır. Bu durum hafif veya şiddetli olabilir. Fakat mide hastalığının türüne bakılmaksızın 1 veya 2 gün içinde iyileşme göstermiyorsa ya da semptomlar şiddetliyse kedinizi hemen veteriner hekime götürün.

 

  Gastritin bozuk yemekten çok hızlı mama yiyip, alerji ve bakteri oluşmasına kadar birçok sebebi vardır. Kediniz kusuyorsa, geğiriyorsa, iştahsızlık ya da idrarı kanlıysa, ishalse veteriner hekime gidin. Tedavi olarak da veteriner hekimiz sıvı tedavisi ve antibiyotik uygular.
	   </p>
