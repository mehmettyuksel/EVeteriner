	   <h2 > 7. Apse Hastalığı</h2>
	    <br>
	   <p >
	   Apse hastalığı daha çok, ev dışında bulunan, sokak kedileri ve sokağa sık çıkan kedilerde görülmektedir. Çevre temizlik şartları nedeniyle iyileşmesi zaman alan bir hastalıktır. Acil tedavi edilmesi gereken bir durumdur.


	   </p>
	    <br>
	   <h2 > 8. Böbrek Hastalıkları</h1>
	    <br>
	   <p >
	   Kedilerde böbrek hastalıkları genellikle idrar yolu enfeksiyonları ile birlikte görülmektedir. Kedilerin karşılaştıkları en yaygın hastalıklar arasında yer almaktadır. Kedilerin bağışıklık sisteminin zayıflaması, çeşitli mamalardan meydana gelmektedir.
	   </p>
