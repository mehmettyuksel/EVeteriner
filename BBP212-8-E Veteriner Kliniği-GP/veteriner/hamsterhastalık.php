	   <h2 > 2.Aspergıllus Mantarı </h2>
	   <br>
	   <p >
	   Bu mantar, hamsterların dışkılarını bıraktığı bölümdeki taban malzemesinde büyür. Bir kere gelişmeye başladığında zamanla beyazdan siyaha dönecektir. Bu süreçte mantar sporlarını hava yoluyla hamsterın çevresine gönderip hamsterın sağlığına zarar verebilecektir. Bu oldukca tehlikelidir çünkü bu sporların hamster tarafından solunması ölüm riskini getirir.
       Semptomları uyuşukluk, nefes alma zorluğu, idrarda kan, iltihaplı veya kızarık cilt ve kronik ishaldir. Diabetik veya sidik torbası enfeksiyonu görülmüş herhangi bir hamster bu mantara karşı özellikle daha fazla risk altındadır. Bu gibi durumda antibiyotik ve anti-mikotik tedaviler uygulanır. Aynı zamanda unutulmamalıdır ki mısır koçanından yapılmış taban malzemeleri, talaştan yapılmış taban malzemelerine göre bu mantarı oluşturmaya daha elverişlidir. Mantarın bir diğer büyüme şekli ise kafeste çürümeye bırakılmış meyve ve sebzelerdir. Mantar oluşumunu engellemeye giden yollardan biri bu mantarın büyümesine izin vermeden hamsterınızın idrarını yaptığı bölgeyi sürekli değiştirmek ve haftada ki bir kere kafesi tamamen temizleyip dezenfekte etmektir, bunun yanında eğer elimizde mevcutsa anti-mantar spreyleri de kullanılabilir. Her zaman yenilmemiş meyve ve sebzeler çürümeye yüz tutup mantar oluşumuna neden olmadan kafesten alınmalıdır. Temiz bir kafes ve düzenli kontrolle bakım yaptıktan sonra bu mantarın oluşması için bir neden yokyur. Bu yüzden temizliği ihmal etmeyin.
	   </p>
