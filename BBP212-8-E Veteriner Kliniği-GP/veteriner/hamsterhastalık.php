	   <h2 > 4. Kanserler ve Tümörler   </h2>
	   <br>
	   <p >
	   Hamsterlerda içsel ya da dışsal kanserli tümörler gelişebilir. Dışsal tümörler genellikle bir bölgede sürekli genişler. Bunun yanında dahili tömürler cerrahi müdaheleye izin verecek kadar erken teşhis edilmeyebilir.
       Semptomları kilo kaybı ve uyuşukluktur. Eğer dışsal bir tümör oluşmuşsa ve başa çıkamayacağı derecede büyümüşse hamsterlerdan anlayan bir veterinere götürüp aldırmalısınız. Bunun yanında bazen hamsterlar bu bölgeyi ısırarak kanatır, tükürüklerinde antibiyotik olduğu için kendi kendilerini de iyleştirebilirler.
