		<h2 > 5.  Yamyamlık  </h2>
		<br>
		<p >
		Gerçekte bir hastalık değildir. Birden fazla hamsterın aynı kafeste tutulması sonucu istemeyen bir kavganın sonucunda ya da yeni doğum yapmış annelerde görülür. Sıklıkla yeni annelerin yavru yemesi yavrularını emzirmek için üretmesi gereken sütü karşılayamacak seviyede besin yetersizliğidir. Böyle bir durumda en zayıf olan yavruyu seçip yer, böylelikle yuvayı dengede tutacak besini sağlamış olur. Çözüm bol bol besin vermek özellikle protein zengini gıdaları (haşlanmış yumurta) eksik etmemektir.

Yavrular büyüdükce tüm aileye yetecek kadar yiyeceğe sahip olamadığını hissederse onların açlıktan ölmesini seyretmek yerine öldürüp yemeyi de tercih edebilir. Bir diğer neden ise annenin kendisini ve yuvasını tehlikede hissetmesidir. Bazen yaralanmış ve hasta yavrular da anne tarafından yenilebilir.

Bir diğer senaryoysa iki yetişkin arasında istenmeyen kavga sonucu birinin ölmesidir. Biri öldüğünde hayatta kalan hamster kavga boyunca sarf enerjiyi geri kazanmak için ölen arkadaşını yiyecektir. Çözüm açıkca görülüyor, ikiden fazla yetişkin syrian hamsterı aynı kafeste tutmayın, bunun yanında birlikte büyümemiş ve farklı kafeslerden alınıp bir kafese konulmuş dwarf hamsterlarında kavgaya eğilimli olacağını unutmayın. Bazı raporların aksine hamsterları etle beslemek onları yamyamlığa teşvik etmez.
		</p>
